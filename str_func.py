def upper_letters(word):
    """
    Принимает на вход строку и
    возвращает ее со всеми заглавными буквами
    """
    return word.upper()


def capitalize_letters(word):
    """
    Принимает на вход строку и
    возвращает ее со всеми заглавными буквами каждого слова
    """
    return word.capitalize()

